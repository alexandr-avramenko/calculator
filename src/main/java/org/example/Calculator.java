package org.example;

public class Calculator {
    public float add(float a, float b) {
        return a + b;
    }

    public float subtraction(float a, float b) {
        return a - b;
    }

    public int division(int a, int b) {
        return a / b;
    }

    public float multiply(float a, float b) {
        return a * b;
    }
}